def add(a, b):
    """Adds two numbers.

    Args:
        a (int): First number.
        b (int): Second number.

    Returns:
        int: The sum of two numbers
    """
    return a + b


def sub(a, b):
    """Subtracts two numbers.

    Args:
        a (int): First number.
        b (int): Second number.

    Returns:
        int: The difference of two numbers
    """
    return a - b


def mul(a, b):
    """Multiplies two numbers.

    Args:
        a (int): First number.
        b (int): Second number.

    Returns:
        int: The multiplication of two numbers
    """
    return a * b


def div(a, b):
    """Divides two numbers.

    Args:
        a (int): First number.
        b (int): Second number.

    Returns:
        int: The division of two numbers
    """
    return a / b
